package InterviewQuestions;

public class Program11 {
	 public static void main(String[] args) {
		System.out.println("Positive Numbers");
		for(int i=0;i<=50;i++)
		{
			if(i%2==0)
			System.out.println(i);
		}
		System.out.println("Negative Numbers");
		for(int i=0;i<=50;i++)
		{
			if(i%2==1)
			System.out.println(i);
		}
	}
}
